IDCORRETOR	INT,
IDCLIENTE  	INT) 

GO
CREATE TABLE [TABCLICOR] (
	[IDCORRETOR] [int] NOT NULL ,
	[IDCLIENTE] [int] NOT NULL ,
	CONSTRAINT [FK_TABCLICOR_IDCLIENTE] FOREIGN KEY 
	(
		[IDCLIENTE]
	) REFERENCES [TABCLIENTES] (
		[IDCLIENTE]
	),
	CONSTRAINT [FK_TABCLICOR_IDCORRETORES] FOREIGN KEY 
	(
		[IDCORRETOR]
	) REFERENCES [TABCORRETORES] (
		[IDCORRETOR]
	)
) ON [PRIMARY]
GO

CREATE TABLE [TABCLIENTES] (
	[IDCLIENTE] [int] IDENTITY (1, 1) NOT NULL ,
	[NOME] [varchar] (150) COLLATE Latin1_General_CI_AS NOT NULL ,
	[CPF] [varchar] (14) COLLATE Latin1_General_CI_AS NOT NULL ,
	[ENDERECO] [varchar] (250) COLLATE Latin1_General_CI_AS NOT NULL ,
	[IDUF] [int] NOT NULL ,
	[IDCIDADE] [int] NOT NULL ,
	[ATIVO] [bit] NULL ,
	 PRIMARY KEY  CLUSTERED 
	(
		[IDCLIENTE]
	)  ON [PRIMARY] ,
	CONSTRAINT [FK_TABCLIENTES_IDCIDADE] FOREIGN KEY 
	(
		[IDCIDADE]
	) REFERENCES [TABCIDADE] (
		[IDCIDADE]
	),
	CONSTRAINT [FK_TABCLIENTES_IDUF] FOREIGN KEY 
	(
		[IDUF]
	) REFERENCES [TABUF] (
		[IDUF]
	)
) ON [PRIMARY]
GO

CREATE TABLE [TABCORRETORES] (
	[IDCORRETOR] [int] IDENTITY (1, 1) NOT NULL ,
	[CODCORRETOR] [varchar] (50) COLLATE Latin1_General_CI_AS NULL ,
