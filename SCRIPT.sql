CREATE TABLE [TABCIDADE] (
	[IDCIDADE] [int] IDENTITY (1, 1) NOT NULL ,
	[NOME] [varchar] (250) COLLATE Latin1_General_CI_AS NOT NULL ,
	[IDUF] [int] NOT NULL ,
	 PRIMARY KEY  CLUSTERED 
	(
		[IDCIDADE]
	)  ON [PRIMARY] ,
	CONSTRAINT [FK_IDUF] FOREIGN KEY 
	(
		[IDUF]
	) REFERENCES [TABUF] (
		[IDUF]
	)
) ON [PRIMARY]
